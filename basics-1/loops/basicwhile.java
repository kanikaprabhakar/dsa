package loops;

public class basicwhile {
    public static void main(String[] args) {
        int i = 1;
        while (i<=9){
            System.out.println(i);
            i++;
        }
        System.out.println("outside while");

        //for 
        for(int j =1;j<=9;++j){
            System.out.println(j);
        }
    }
}
